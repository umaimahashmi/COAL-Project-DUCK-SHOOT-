MAIN_MENU proc
    ; Set video mode
    mov al, 13h
    mov ah, 00h
    int 10h
    ; Draw different parts of the screen (function calls assumed to be defined elsewhere)
    call draw_D
    call draw_U
    call draw_C
    call draw_K
    call draw_S
    call draw_H
    call draw_O
    call draw_O1
    call draw_T
    call draw_LINE
    call draw_cloud
    call draw_cloud1
    call draw_cloud2
    call draw_GAME1
    call draw_GAME2
    call draw_ONE
    call draw_TWO
    call draw_DUCK1
    call draw_DUCK2
    call draw_PRESS
    call draw_o_or_t
    call draw_zero
    call draw_menuexit
    ;print players name
    ;MOV AH,9
    ;LEA DX,PLAYER_NAME
    ;INT 21H
    ;*******************************************************************
    ;conditions to move  onto the next page
    L2:
        mov ah, 00h
        int 16h
        mov dl, al  ; Read key press into dl

        cmp dl, '0'
        je call_game_title
        cmp dl, '1'  ; Compare with ASCII code for '1'
        je call_one_duck  ; If it is '1', jump to handle it
        cmp dl, '2'  ; Compare with ASCII code for '2'
        je call_two_duck  ; If it is '2', jump to handle it

        ; Continue the loop if the key press is neither '1' nor '2'
        jmp L2
    ; Handle key press of '1'
    call_one_duck:
        call ONE_DUCK
    ; Handle key press of '2'
    call_two_duck:
        call TWO_DUCK
    ; Return from the main menu procedure
    call_game_title:
        call GAME_TITLE
    ;**********************************************************************
    ret
MAIN_MENU endp

